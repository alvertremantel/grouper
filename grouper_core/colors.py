def lerp_hex(c1: str, c2: str, t: float) -> str:
    """Linearly interpolate between two #RRGGBB hex colors. t is clamped to [0, 1]."""
    t = max(0.0, min(1.0, t))
    r1, g1, b1 = int(c1[1:3], 16), int(c1[3:5], 16), int(c1[5:7], 16)
    r2, g2, b2 = int(c2[1:3], 16), int(c2[3:5], 16), int(c2[5:7], 16)
    r = int(r1 + (r2 - r1) * t)
    g = int(g1 + (g2 - g1) * t)
    b = int(b1 + (b2 - b1) * t)
    return f"#{r:02x}{g:02x}{b:02x}"


def theme_colors(theme: str) -> dict[str, str]:
    """Return programmatic color tokens for *theme*, falling back to dark."""
    return _THEME_PALETTE.get(theme, _THEME_PALETTE["dark"])


def available_themes() -> list[str]:
    """Return names of all registered themes."""
    return sorted(_THEME_PALETTE.keys())


